     // We'll create a program that converts gallons to liters.

public class GalToLit {
    public static void main(String args[]){
        double gallons;   //   detine numarul de galloane
        double liters;  //   detine conversia la litri
        gallons = 10;
        liters = gallons * 3.7854;  //  convertim la litri
        System.out.println(gallons + " gallons is " + liters + " liters ");

    }

}
